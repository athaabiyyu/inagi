  <script>
		document.addEventListener("DOMContentLoaded", function () {
			const articlesList = document.querySelector("#articles-splide .splide__list"); // Target list untuk post
			const apiURL = "https://www.indopacking.com/wp-json/wp/v2/posts?per_page=10&_embed"; // API endpoint baru

			fetch(apiURL)
				.then(response => response.json())
				.then(posts => {
					articlesList.innerHTML = ""; // Kosongkan container sebelum menambahkan data baru

					posts.forEach(post => {
						const postDate = new Date(post.date).toLocaleDateString();
						const postTitle = post.title.rendered.replace(/&quot;/g, '"'); // Escape karakter spesial
						const maxChars = 100; 
						const postExcerpt = post.excerpt.rendered.replace(/<[^>]*>?/gm, '').substring(0, maxChars) + "...";
						const postLink = post.link;

						// Cek apakah featured image tersedia
						const postImage = post._embedded?.["wp:featuredmedia"]?.[0]?.source_url || "https://via.placeholder.com/300x200?text=No+Image";

						// Buat elemen list untuk artikel
						const postHTML = `
							<li class="splide__slide px-2">
								<div class="card border border-gray-200 h-full hover:shadow-md transition-shadow">
									<div class="card-image-container rounded-t-lg">
										<img 
											src="${postImage}" 
											alt="${postTitle}"
											class="article-card-image"
										/>
										<div class="absolute top-2 right-2">
											<span class="article-category bg-white text-gray-700">Blog</span>
										</div>
									</div>
									<div class="p-4">
										<p class="article-date mb-2">${postDate}</p>
										<h3 class="text-xl font-semibold mb-2">${postTitle}</h3>
										<p class="text-gray-600 text-sm mb-4">${postExcerpt}</p>
										<a href="${postLink}" target="_blank" class="text-black font-medium hover:underline flex items-center">
											Baca Selengkapnya
										</a>
									</div>
								</div>
							</li>
						`;

						articlesList.innerHTML += postHTML; // Tambahkan ke dalam list artikel
					});

					// ðŸ”„ Inisialisasi Splide setelah data dimuat
					initializeSplide();
				})
				.catch(error => console.error("Error fetching blog posts:", error));
		});

